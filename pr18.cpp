#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    int x;
    cout<<"Enter a value: ";
    cin>>x;

    cout<<"Post decrement"<<endl;

    cout<<"First use of x is: "<<x<<endl;

    x--;

    cout<<"Second use of x is: "<<x<<endl;

    cout<<"Pre decrement"<<endl;

    cout<<"First use of x is: "<<x<<endl;

    --x;

    cout<<"Second use of x is: "<<x;


}

